package me.ayydxn.luminescence.geometry;

/**
 * Integer Rectangle Helper.
 *
 * @author Ayydxn
 */
public class IntRect
{
    public int left;
    public int top;
    public int right;
    public int bottom;

    public IntRect(int left, int top, int right, int bottom)
    {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }
}
