package me.ayydxn.luminescence.geometry;

/**
 * Float Rectangle Helper.
 *
 * @author Ayydxn
 */
public class ULRect
{
    public float left;
    public float top;
    public float right;
    public float bottom;

    public ULRect(float left, float top, float right, float bottom)
    {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }
}
